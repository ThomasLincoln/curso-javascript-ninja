// também existe a notação -- ou ++

var soma = 20
soma++ //soma vai ser 21, pq adcionou 1 nela
soma-- // soma vai voltar a ser 20, pois subtraiu 1


// também é possível abreviar eles, assim ó

soma += 2; // vai somar 2 no valor de soma
soma *= 2 //vai multiplicar soma por 2

// e isso vale para todas as operações